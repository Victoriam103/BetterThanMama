<h4>Curiosidades: </h4>
 <h5>1. ¡Aún están vivas cuando las compra!  </h5>
 
 <p>A diferencia de lo que ocurre, por ejemplo, con una zanahoria cosechada, las patatas aún están vivas cuando las ve en el supermercado. Si alguna vez ha guardado patatas en un lugar cálido y húmedo, habrá visto que les salen brotes. Pero, ¿sabía que si volviese a colocar las patatas en la tierra esos brotes crecerían hasta convertirse en una nueva patata? </p>
  
 <h5>2. Las patatas fueron la primera hortaliza que se cultivó en el espacio.  </h5>
  
 <p>La NASA probó por primera vez las patatas de siembra en 1995 y, en 2004, empleó una técnica china para cultivarlas en una cámara. ¡Así que podríamos decir que las patatas son un alimento de otro mundo!  </p>
  
 <h5>3. El boniato no es realmente un tipo de patata.  </h5>
  
 <p>El boniato es muy similar a la patata tanto por su aspecto como por su textura. Sin embargo, en realidad pertenece a la familia Convolvulaceae, como las campanillas. Esto significa que, aunque son parientes lejanos, el boniato está más cerca de ser una flor que una patata. <p>
  
 <h5>4. La patata es una hortaliza camaleónica.  </h5>
  
<p> No todas las patatas son amarillas, sino que también hay variedades rojas, doradas o incluso moradas. ¡Hasta pueden cambiar de color! Si no se mantienen en condiciones óptimas, su piel dejará de tener ese tono marrón y adquirirá un color verde. Pero no se las coma cuando estén verdes, pues el color es una advertencia de que podrían presentar una toxina llamada solanina. </p>
  
 <h5>5. Las personas y las patatas tenemos algo en común.  </h5>
  
 <p>Aunque a simple vista no lo parezca, las patatas contienen alrededor de un 80 % de agua: ¡casi la misma cantidad que el cuerpo humano! Podría exprimirlas igual que una manzana, aunque probablemente no estarían muy buenas.  </p>
  
 <h5> 6. Las patatas son un alimento ecológico. </h5>
  
 <p>Como son tan fáciles de cultivar, no necesitan una gran cantidad de fertilizantes ni de otros productos químicos. Además, producen muy pocas emisiones de gases de efecto invernadero. Esto hace que sean una fuente de carbohidratos más ecológica que el arroz y los cereales que se utilizan para alimentos como la pasta o el pan.  <p>
  
 <p>Como ve, esta humilde hortaliza es seguramente un poco más interesante de lo que creía. La próxima vez que le pongan patatas fritas en el plato, recuerde que son algo más que una simple guarnición deliciosa. 
</p>

